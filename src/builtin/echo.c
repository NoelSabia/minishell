#include "../../include/minishell.h"

int	ft_echo(t_command *cmd)
{
	int	i;

	i = 0;
	if (ft_strncmp(cmd->command, "echo", 4) != 0)
		return (0);
	printf("cmd->args[0]: %s\n", cmd->args[0]);
	while (cmd->args[i])
	{
		printf("%s", cmd->args[i]);
		if (cmd->args[i + 1])
			printf(" ");
		i++;
	}
	printf("\n");
	return (1);
}
